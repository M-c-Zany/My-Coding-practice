
# Example 1: Integer
num = 42
print(type(num))

# Example 2: Float
float_num = 3.14
print(type(float_num))

# Example 3: String
text = "Hello, World!"
print(type(text))

# Example 4: List
my_list = [1, 2, 3, 4]
print(type(my_list))

# Example 5: Tuple
my_tuple = (1, 2, 3, 4)
print(type(my_tuple))

# Example 6: Dictionary
my_dict = {'a': 1, 'b': 2}
print(type(my_dict))

# Example 7: Set
my_set = {1, 2, 3, 4}
print(type(my_set))

# Example 8: Boolean
is_true = True
print(type(is_true))

# Example 9: NoneType
none_value = None
print(type(none_value))

# Example 10: Custom Class
class MyClass:
    pass

obj = MyClass()
print(type(obj))

# Example 11: Function
def my_function():
    pass

print(type(my_function))

# Example 12: Module
import math
print(type(math))

# Example 13: File
file_obj = open("example.txt", "w")
print(type(file_obj))

# Example 14: Complex Number
complex_num = 1 + 2j
print(type(complex_num))

# Example 15: Byte
byte_data = b'hello'
print(type(byte_data))


