# 1. Numeric Types:
# int: Integer data type represents whole numbers.
x = 5
y = -10


# - float: Float data type represents decimal numbers.
pi = 3.14
salary = 2500.50


# complex: Complex data type represents numbers in the form `a + bj`, where `a` and `b` are floats.
z = 3 + 4j


# 2. Text Type:
# - str: String data type represents a sequence of characters.
name = "John"
message = 'Hello, World!'


# 3. Sequence Types:
# - list: List is a mutable sequence of elements.
numbers = [1, 2, 3, 4, 5]
names = ['Alice', 'Bob', 'Charlie']


# tuple: Tuple is an immutable sequence of elements.
coordinates = (3, 4)
colors = ('red', 'green', 'blue')


# range: Range represents an immutable sequence of numbers.
my_range = range(5)


# 4. Mapping Type:
# - dict: Dictionary is an unordered collection of key-value pairs.
person = {'name': 'John', 'age': 30, 'city': 'New York'}


# 5. Set Types:
# - set: Set is an unordered collection of unique elements.
my_set = {1, 2, 3, 4, 5}


# 6. Boolean Type:
# - bool: Boolean data type represents the truth values `True` or `False`.
is_raining = True
has_sunshine = False


# 7. None Type:
#NoneType:Represents the absence of a value.
result = None
    
